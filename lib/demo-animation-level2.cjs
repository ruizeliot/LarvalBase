let frameIndex = 0;
let loopCount = 0;
const maxLoops = Infinity;

function clearScreen() {
  process.stdout.write('\x1B[2J\x1B[H');
}

function hideCursor() {
  process.stdout.write('\x1B[?25l');
}

function showCursor() {
  process.stdout.write('\x1B[?25h');
}

function render() {
  clearScreen();

  // Header with gradient effect
  console.log('\x1B[38;5;51m╔════════════════════════════════════════════════════════════════════════╗\x1B[0m');
  console.log('\x1B[38;5;45m║  LEVEL 2: Detailed Animation - Full Mock UI                           ║\x1B[0m');
  console.log('\x1B[38;5;39m║  User Story: "User drags node from palette to canvas"                 ║\x1B[0m');
  console.log('\x1B[38;5;33m╚════════════════════════════════════════════════════════════════════════╝\x1B[0m');

  // Frame content with white text
  console.log('\x1B[97m' + frames[frameIndex] + '\x1B[0m');

  // Footer
  console.log('\x1B[90m──────────────────────────────────────────────────────────────────────────\x1B[0m');
  console.log(`\x1B[33m  Frame ${frameIndex + 1}/${frames.length}\x1B[0m | \x1B[36mLoop ${loopCount + 1}/${maxLoops}\x1B[0m | \x1B[90mPress Ctrl+C to exit\x1B[0m`);

  frameIndex++;

  if (frameIndex >= frames.length) {
    frameIndex = 0;
    loopCount++;

    if (loopCount >= maxLoops) {
      clearScreen();
      console.log('\n\x1B[32m  ✓ Level 2 Animation complete!\x1B[0m');
      console.log('\x1B[90m    Shows: tooltips, status bar, drop zones, config panel, I/O ports\x1B[0m\n');
      showCursor();
      setTimeout(() => process.exit(0), 3000);
      return;
    }
  }

  // Slower for detailed frames
  setTimeout(render, 800);
}

process.on('SIGINT', () => {
  showCursor();
  console.log('\n\x1B[0m');
  process.exit(0);
});

hideCursor();
render();
